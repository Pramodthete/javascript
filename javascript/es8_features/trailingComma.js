// Array with trailing comma
const colors = ['red', 'green', 'blue',];

console.log(colors); // Output: ['red', 'green', 'blue']

// Object literal with trailing comma
const person = {
    firstName: 'John',
    lastName: 'Doe',
    age: 30,
};

console.log(person); // Output: { firstName: 'John', lastName: 'Doe', age: 30 }
